# 数字->字符串
num_str = str(111)
print(type(num_str), num_str)

float_str = str(11.1)
print(type(float_str), float_str)

# 字符串->数字
num1 = int("222")
print(type(num1), num1)

num2 = float("22.2")
print(type(num2), num2)

# 整数->浮点数
float_num = float(333)
print(type(float_num), float_num)

# 浮点数->整数，注意：丢失小数部分
int_num = int(44.4)
print(type(int_num), int_num)
